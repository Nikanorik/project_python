flag = 'y'
def caeser(start_text, shift_amount, new_direction):
    string = ''
    for i in start_text:
        if i in alphabet:
            if new_direction == 'encode':
                index_str = alphabet.index(i)+shift_amount
            elif new_direction == 'decode':
                index_str = alphabet.index(i) - shift_amount
            string += alphabet[index_str]
        else:
            string+=i
    print(string)

while flag == 'y':
    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
    text = input("Type your message:\n").lower()
    shift = int(input("Type the shift number:\n"))

    caeser(start_text=text, shift_amount=shift, new_direction=direction)
    flag = input('Next? Enter y or n: ').lower()

